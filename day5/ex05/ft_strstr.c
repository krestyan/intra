char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] != to_find[j])
		{
			i++;
			j = 0;
		}
		else
		{
			if (to_find[j + 1] == 0)
				return (&str[i - j]);
			i++;
			j++;
		}
	}
	return (0);
}
